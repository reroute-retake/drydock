// Package gen renders the per-space LiteLLM gateway config and docker-compose
// file from a manifest. Both outputs are deterministic and unit-tested; the
// dock commands write them into the space's .drydock/ dir before `up`.
package gen

import (
	"bytes"
	"path/filepath"
	"sort"
	"strings"
	"text/template"

	"github.com/reroute-retake/drydock/internal/config"
	"github.com/reroute-retake/drydock/internal/paths"
	"github.com/reroute-retake/drydock/internal/ports"
)

// envKey maps a provider to the env var holding its API key.
func envKey(provider string) string {
	switch provider {
	case "anthropic":
		return "ANTHROPIC_API_KEY"
	case "openai":
		return "OPENAI_API_KEY"
	default:
		return strings.ToUpper(provider) + "_API_KEY"
	}
}

type litellmModel struct {
	Alias, Provider, Model, APIBase, EnvKey string
}

var litellmTmpl = template.Must(template.New("litellm").Parse(
	`# Generated by dock — do not edit by hand. Source: space.yaml
model_list:
{{- range .Models}}
  - model_name: {{.Alias}}
    litellm_params:
      model: {{.Provider}}/{{.Model}}
{{- if .APIBase}}
      api_base: {{.APIBase}}
{{- else}}
      api_key: os.environ/{{.EnvKey}}
{{- end}}
{{- end}}

litellm_settings:
  drop_params: true
  num_retries: 2
  callbacks: ["drydock_logger.instance"]

general_settings:
  master_key: os.environ/LITELLM_MASTER_KEY
`))

// LiteLLMLogger returns the source of the LiteLLM custom callback that appends
// one drydock telemetry event per model call to $DRYDOCK_TELEMETRY_DIR/gateway/
// events.jsonl. dock writes this beside the gateway config and mounts it in.
// (Verify field names against the installed LiteLLM on a Docker host.)
func LiteLLMLogger() string { return liteLLMLoggerSrc }

const liteLLMLoggerSrc = `# Generated by dock — LiteLLM custom logger (drydock telemetry).
import os, json, datetime, threading
from litellm.integrations.custom_logger import CustomLogger

_lock = threading.Lock()

def _events_path():
    base = os.environ.get("DRYDOCK_TELEMETRY_DIR", "/telemetry")
    d = os.path.join(base, "gateway")
    os.makedirs(d, exist_ok=True)
    return os.path.join(d, "events.jsonl")

def _meta(kwargs):
    return (kwargs.get("litellm_params") or {}).get("metadata") or {}

def _write(ev):
    try:
        with _lock:
            with open(_events_path(), "a") as f:
                f.write(json.dumps(ev) + "\n")
    except Exception:
        pass

class DrydockLogger(CustomLogger):
    def log_success_event(self, kwargs, response_obj, start_time, end_time):
        slo = kwargs.get("standard_logging_object") or {}
        m = _meta(kwargs)
        ev = {
            "ts": datetime.datetime.utcnow().isoformat() + "Z",
            "session_id": m.get("session_id", ""), "space": m.get("space", ""),
            "ticket": m.get("ticket", ""), "phase": m.get("phase", ""),
            "skill": m.get("skill", ""), "event_type": "llm_call",
            "model": slo.get("model") or kwargs.get("model", ""),
            "tokens_in": slo.get("prompt_tokens", 0),
            "tokens_out": slo.get("completion_tokens", 0),
            "cost_usd": slo.get("response_cost", 0.0), "status": "ok",
        }
        try:
            ev["duration_ms"] = int((end_time - start_time).total_seconds() * 1000)
        except Exception:
            pass
        _write(ev)

    def log_failure_event(self, kwargs, response_obj, start_time, end_time):
        m = _meta(kwargs)
        _write({
            "ts": datetime.datetime.utcnow().isoformat() + "Z",
            "session_id": m.get("session_id", ""), "space": m.get("space", ""),
            "ticket": m.get("ticket", ""), "phase": m.get("phase", ""),
            "skill": m.get("skill", ""), "event_type": "failure",
            "model": kwargs.get("model", ""), "status": "error",
        })

instance = DrydockLogger()
`

// LiteLLMConfig renders the gateway config (one OpenAI-compatible endpoint
// fronting every model role). Roles are emitted in sorted order for stability.
func LiteLLMConfig(m *config.Manifest) (string, error) {
	roles := make([]string, 0, len(m.Models))
	for r := range m.Models {
		roles = append(roles, r)
	}
	sort.Strings(roles)
	models := make([]litellmModel, 0, len(roles))
	for _, r := range roles {
		mv := m.Models[r]
		models = append(models, litellmModel{
			Alias: r, Provider: mv.Provider, Model: mv.Model,
			APIBase: mv.APIBase, EnvKey: envKey(mv.Provider),
		})
	}
	var buf bytes.Buffer
	if err := litellmTmpl.Execute(&buf, struct{ Models []litellmModel }{models}); err != nil {
		return "", err
	}
	return buf.String(), nil
}

type composeData struct {
	Space, Drydock, Env, LiteLLM, Repos, Vault, Works, Telemetry string
	GatewayPort                                                  int
	Ports                                                        []int
}

// GatewayHostPort returns the host port a space's gateway is published on:
// the manifest's gateway_port if set, otherwise a deterministic per-space port.
func GatewayHostPort(m *config.Manifest) int {
	if m.GatewayPort != 0 {
		return m.GatewayPort
	}
	return ports.GatewayPort(m.Space)
}

var composeTmpl = template.Must(template.New("compose").Parse(
	`# Generated by dock — do not edit by hand. Source: space.yaml
name: drydock-{{.Space}}
services:
  gateway:
    image: ghcr.io/berriai/litellm:main-stable
    command: ["--config", "/etc/litellm/config.yaml", "--port", "4000"]
    ports:
      - "{{.GatewayPort}}:4000"
    environment:
      DRYDOCK_TELEMETRY_DIR: "/telemetry"
      PYTHONPATH: "/etc/litellm"
    volumes:
      - "{{.LiteLLM}}:/etc/litellm/config.yaml:ro"
      - "{{.Drydock}}/drydock_logger.py:/etc/litellm/drydock_logger.py:ro"
      - "{{.Telemetry}}:/telemetry"
    env_file: ["{{.Env}}"]
    extra_hosts: ["host.docker.internal:host-gateway"]
  dev:
    build:
      context: "{{.Drydock}}"
      dockerfile: Dockerfile
    image: "drydock-{{.Space}}:dev"
    depends_on: [gateway]
    environment:
      OPENAI_URL: "http://gateway:4000/v1"
      OPENAI_API_KEY: "${LITELLM_MASTER_KEY}"
    env_file: ["{{.Env}}"]
    extra_hosts: ["host.docker.internal:host-gateway"]
    volumes:
      - "{{.Repos}}:/workspace/repos"
      - "{{.Vault}}:/workspace/vault"
      - "{{.Works}}:/workspace/works"
      - "{{.Telemetry}}:/workspace/telemetry:ro"
{{- if .Ports}}
    ports:
{{- range .Ports}}
      - "{{.}}:{{.}}"
{{- end}}
{{- end}}
    working_dir: /workspace
    command: ["sleep", "infinity"]
`))

// ComposeFile renders the docker-compose for a space: a LiteLLM gateway sidecar
// plus the dev container, with repos/vault/works mounted and ports published.
func ComposeFile(m *config.Manifest, sp paths.Space) (string, error) {
	data := composeData{
		Space:       m.Space,
		Drydock:     sp.Drydock,
		Env:         sp.Env(),
		LiteLLM:     sp.LiteLLM(),
		Repos:       sp.Repos,
		Vault:       sp.Vault,
		Works:       sp.Works,
		Telemetry:   filepath.Join(paths.StateHome(), "sessions", m.Space),
		GatewayPort: GatewayHostPort(m),
		Ports:       m.Ports,
	}
	var buf bytes.Buffer
	if err := composeTmpl.Execute(&buf, data); err != nil {
		return "", err
	}
	return buf.String(), nil
}

// stackLayers maps an image-layer id to Dockerfile RUN lines installing its
// toolchain. These are debian/apt starting points — pin exact versions to taste.
var stackLayers = map[string][]string{
	"jdk21-maven":  {"RUN apt-get update && apt-get install -y --no-install-recommends default-jdk maven && rm -rf /var/lib/apt/lists/*"},
	"jdk21-gradle": {"RUN apt-get update && apt-get install -y --no-install-recommends default-jdk gradle && rm -rf /var/lib/apt/lists/*"},
	"node":         {"RUN apt-get update && apt-get install -y --no-install-recommends nodejs npm && rm -rf /var/lib/apt/lists/*"},
	"go":           {"RUN apt-get update && apt-get install -y --no-install-recommends golang-go && rm -rf /var/lib/apt/lists/*"},
	"python":       {"RUN apt-get update && apt-get install -y --no-install-recommends python3 python3-pip python3-venv && rm -rf /var/lib/apt/lists/*"},
	"rust":         {"RUN apt-get update && apt-get install -y --no-install-recommends rustc cargo && rm -rf /var/lib/apt/lists/*"},
}

// Dockerfile renders the per-space dev image: base + common tools + ForgeCode +
// one layer per entry in image.stacks (design doc 7.6). Unknown stack ids are
// emitted as comments so the file still builds.
func Dockerfile(m *config.Manifest) string {
	var b strings.Builder
	b.WriteString("# Generated by dock — do not edit by hand. Source: space.yaml\n")
	b.WriteString("FROM " + m.Image.Base + "\n")
	b.WriteString("RUN apt-get update && apt-get install -y --no-install-recommends ca-certificates curl git jq && rm -rf /var/lib/apt/lists/*\n")
	if v := m.Forge.Version; v != "" {
		// FORGE_VERSION is the pin hook; confirm the installer honors it.
		b.WriteString("# ForgeCode pinned to " + v + "\n")
		b.WriteString("RUN curl -fsSL https://forgecode.dev/cli | FORGE_VERSION=" + v + " sh || true\n")
	} else {
		b.WriteString("RUN curl -fsSL https://forgecode.dev/cli | sh || true\n")
	}
	b.WriteString(`ENV PATH="/root/.forge/bin:${PATH}"` + "\n")
	for _, id := range m.Image.Stacks {
		b.WriteString("# stack: " + id + "\n")
		lines, ok := stackLayers[id]
		if !ok {
			b.WriteString("# (no known layer for this stack id — add one in internal/gen)\n")
			continue
		}
		for _, l := range lines {
			b.WriteString(l + "\n")
		}
	}
	b.WriteString("WORKDIR /workspace\n")
	return b.String()
}
